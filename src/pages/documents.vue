                    реализация мероприятий по информационному освещению инвестиционных возможностей области;
                  </li>
                  <li>развитие особых экономических зон;</li>
                  <li>создание и развитие индустриальных парков;</li>
                  <li>развитие государственно-частного партнёрства;</li>
