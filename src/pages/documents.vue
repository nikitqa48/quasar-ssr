                <div class="text-h4 q-mb-md">Cтандарт развития конкуренции</div>
                <p>Конкуренция является стимулом экономического роста страны и развития инноваций. Активное содействие развитию конкуренции в Российской Федерации определено приоритетным направлением деятельности всех органов власти согласно Указу Президента РФ от 21.12.2017 № 618 «Об основных направлениях государственной политики по развитию конкуренции (далее – Указ).</p>
                <p>В Указе обозначены новые цели и задачи. В частности, во всех отраслях экономики должны быть созданы благоприятные условия для развития бизнеса, снижено количество нарушений антимонопольного законодательства органами государственной власти и местного самоуправления. Субъектам Российской Федерации необходимо увеличить долю закупок, участниками которых являются представители малого и среднего предпринимательства и социально ориентированные некоммерческие организации.</p>
                <p>В целях обеспечения единых подходов к развитию конкуренции во всех регионах Российской Федерации распоряжением Правительства РФ утвержден новый Стандарт развития конкуренции в субъектах Российской Федерации от 17.04.2019 № 768-р. Цель принятого решения – повысить эффективность работы региональных властей в области развития конкуренции. (Стандарт развития конкуренции в субъектах Российской Федерации от 05.09.2015 № 1738-р признан утратившим силу).</p>
