                  <li>
                    формирование благоприятного инвестиционного климата в Липецкой области;
                    совершенствование нормативно-правовой базы, регулирующей инвестиционную деятельность;
                  </li>
                  <li>повышение эффективности использования мер государственной поддержки инвестиционной деятельности;</li>

                  <li>
